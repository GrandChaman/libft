int	ft_islowercase(char *str)
{
	int res;
	int i;

	i = -1;
	res = 1;
	while (str[++i])
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			res = 0;
			break ;
		}
	}
	return (res);
}
